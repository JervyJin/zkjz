<script>
import area from './area'
export default {
  name: "Menu",
  data() {
    return {
      // 显示状态默认值
      radio: "1",
      // 遮罩层
      loading: true,
      //   下拉菜单数据
      options: [
        {
          value: "选项1",
          label: "黄金糕"
        },
        {
          value: "选项2",
          label: "双皮奶"
        },
        {
          value: "选项3",
          label: "蚵仔煎"
        },
        {
          value: "选项4",
          label: "龙须面"
        },
        {
          value: "选项5",
          label: "北京烤鸭"
        }
      ],
      value: "",

      // 菜单表格树数据
      // menuList: [
      //   {
      //     id: 1,
      //     menuName: "中科九洲科技",
      //     orderNum: "1",
      //     status: "显示",
      //     children: [
      //       {
      //         id: 31,
      //         menuName: "服务事业部",
      //         orderNum: "1-1",
      //         status: "显示",
      //         children: [
      //           {
      //             id: 41,
      //             menuName: "客服中心",
      //             orderNum: "1-1-1",
      //             status: "显示"
      //           }
      //         ]
      //       },
      //       {
      //         id: 32,
      //         menuName: "其他",
      //         orderNum: "1-2",
      //         status: "显示"
      //       }
      //     ]
      //   },
      // ],
      menuList : area.areaList,
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 显示状态数据字典
      visibleOptions: [],
      // 菜单状态数据字典
      statusOptions: [
          {dictValue:'1',dictLabel:'显示'},
          {dictValue:'2',dictLabel:'隐藏'},
      ],
      // 查询参数
      queryParams: {
        menuName: undefined,
        visible: undefined
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        menuName: [
          { required: true, message: "地区名称不能为空", trigger: "blur" }
        ],
        orderNum: [
          { required: true, message: "地区顺序不能为空", trigger: "blur" }
        ],
      }
    };
  },
  created() {
    this.getList();
  },
  methods: {
    // 选择图标
    selected(name) {
      this.form.icon = name;
    },
    /** 查询菜单列表接口 */
    getList() {},
    /** 转换菜单数据结构 */
    normalizer(node) {
      if (node.children && !node.children.length) {
        delete node.children;
      }
      return {
        id: node.menuId,
        label: node.menuName,
        children: node.children
      };
    },
    /** 查询菜单下拉树结构 */
    getTreeselect() {},
    // 显示状态字典翻译
    visibleFormat(row, column) {
      if (row.menuType == "F") {
        return "";
      }
      return this.selectDictLabel(this.visibleOptions, row.visible);
    },
    // 菜单状态字典翻译
    statusFormat(row, column) {
      if (row.menuType == "F") {
        return "";
      }
      return this.selectDictLabel(this.statusOptions, row.status);
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
     // dialog关闭，表单验证重置
    formDialogClosed(){
      this.$refs.form.resetFields()

    },
    // 表单重置
    reset() {
      this.form = {
        menuId: undefined,
        value:undefined,
        parentId: 0,
        menuName: undefined,
        icon: undefined,
        menuType: "M",
        orderNum: undefined,
        isFrame: "1",
        visible: "0",
        status: "0"
      };
      //   this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.getList();
    },
    /** 新增按钮操作 */
    handleAdd(row) {
      this.reset();
      this.getTreeselect();
      if (row != null) {
        this.form.parentId = row.menuId;
      }
      this.open = true;
      this.title = "添加部门";
    },
    /** 修改按钮操作
     * ①请求接口获取当前数据 把数据赋值给dialog里的form
     * ②在请求回调中打开弹窗并修改title
     */
    handleUpdate(row) {
      this.reset();
      this.getTreeselect();
        this.open = true;
        this.title = "修改部门";
    },
    /** 提交按钮 */
    submitForm: function() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.menuId != undefined) {
            updateMenu(this.form).then(response => {
              if (response.code === 200) {
                this.msgSuccess("修改成功");
                this.open = false;
                this.getList();
              } else {
                this.msgError(response.msg);
              }
            });
          } else {
            addMenu(this.form).then(response => {
              if (response.code === 200) {
                this.msgSuccess("新增成功");
                this.open = false;
                this.getList();
              } else {
                this.msgError(response.msg);
              }
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      this.$confirm(
        '是否确认删除名称为"' + row.menuName + '"的数据项?',
        "警告",
        {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning"
        }
      )
        .then(function() {
          return delMenu(row.menuId);
        })
        .then(() => {
          this.getList();
          this.msgSuccess("删除成功");
        })
        .catch(function() {});
    }
  }
};
</script>
